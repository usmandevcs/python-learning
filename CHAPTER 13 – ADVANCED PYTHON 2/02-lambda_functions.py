# LAMBDA FUNCTIONS:
'''
Ye anonymous (naam ke bina) functions hote hain jo sirf ek 
line me kaam karte hain.
'''
# SYNTAX:
# lambda arguments: expression

# EXAMPLE 1:
square = lambda x: x*x
print(square(6))  # output: 36

# EXAMPLE 2:
sum_three = lambda a, b, c: a + b + c
print(sum_three(1, 2, 3))  # output: 6
