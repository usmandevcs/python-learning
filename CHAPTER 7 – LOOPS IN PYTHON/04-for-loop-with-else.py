# FOR LOOP WITH ELSE 
'''An optional else can be used with a for loop if the code
is to be executed when the loops exhausts. means k hum else
ko for loop ma use kar skty hain else tab execute hoga jab 
for loop completely execute ho jaye ga pehly '''
# Example: 
l= [1,7,8] 
for item in l:
    print(item) 
else: 
    print("done") # this is printed when the loop exhausts!
# Output: 
'''1 
7 
8 
done'''


