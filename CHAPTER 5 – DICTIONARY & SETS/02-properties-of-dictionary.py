'''
Properties of Dictionary in Python
1. Unordered: Dictionaries do not maintain
any order for the key-value pairs.
2. Mutable: Dictionaries can be changed
after their creation.
3. Indexed by Keys: Values are accessed
using keys.
4. No Duplicate Keys: Each key must be unique.
5. Heterogeneous: Keys and values can be
of different data types.
6. Dynamic Size: Dictionaries can grow
or shrink as needed.
'''