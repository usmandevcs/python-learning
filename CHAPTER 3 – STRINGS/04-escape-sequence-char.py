""" 1st escape sequence character is \n which is used to 
add a new line in a string"""
a = "Hello\nWorld"
print(a)
""" 2nd escape sequence character is \t which is used to 
add a tab space in a string"""
a = "Hello\tWorld"
print(a)
""" 3rd escape sequence character is \\ which is used to 
add a backslash in a string"""
a = "Hello\\World"
print(a)
""" 4th escape sequence character is \' which is used to 
add a single quote in a string"""
a = 'Hello\'World'
print(a)
""" 5th escape sequence character is \" which is used to 
add a double quote in a string"""  
a = "Hello\"World"
print(a)
""" 6th escape sequence character is \r which is used to 
add a carriage return in a string"""
a = "Hello\rWorld"  
print(a)
""" 7th escape sequence character is \b which is used to 
add a backspace in a string"""
a = "Hello\bWorld"
print(a)
""" 8th escape sequence character is \f which is used to 
add a form feed in a string"""
a = "Hello\fWorld"
print(a)